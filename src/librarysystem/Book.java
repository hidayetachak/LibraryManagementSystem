package librarysystem;

/**
 *
 * @author Hidayet
 */
public class Book 

    {
    private String name;
    private double price;
    private int id;

    public Book(String name, double price, int id) {
        this.name = name;
        this.price = price;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public int getId() {
        return id;
    }
}
    

